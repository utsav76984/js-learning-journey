// The while loop repeatedly executes a block of code as long as a specified condition is true.

// while loop example

let i1 = 0;

while (i1 < 10) {
  console.log("i1", i1);
  i1++;
}

// another example

let sum = 0;

let i = 1;
while (i <= 100) {
  sum += i;
  i++;
  console.log(sum);
}